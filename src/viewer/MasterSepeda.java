    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       try {
           String idSpd = jTextField1.getText();
           String namaSpd = jTextField2.getText();
           String modelSpd = jTextField3.getText();
           String sts = jTextField4.getText();
           String jmlh = jTextField5.getText();
           
           Configuration configuration = new Configuration().configure();
           StandardServiceRegistry serviceRegistry = new StandardServiceRegistryBuilder()
                   .applySettings(configuration.getProperties()).build();
           SessionFactory sf = configuration.buildSessionFactory(serviceRegistry);
           
           StokSepeda spd = new StokSepeda(idSpd, namaSpd, modelSpd, sts, jmlh);
           Session s = sf.openSession();
           s.beginTransaction();
           s.saveOrUpdate(spd);
           s.getTransaction().commit();
           sf.close();
           JOptionPane.showMessageDialog(null, "Simpan Data Berhasil");
       } catch(HibernateException e) {
           JOptionPane.showMessageDialog(null, e.getMessage());
       }
    }//GEN-LAST:event_jButton1ActionPerformed

